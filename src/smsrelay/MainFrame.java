    public void CarregaGrupos(){  
        jComboBoxGrps.removeAllItems();
        if (conexao.conectado){
            Statement s;
            jComboBoxGrps.addItem("Todos os Grupos");
            try {
                s = conexao.conn.createStatement();
                s.execute("select Nome from grupos");
                ResultSet rs = s.getResultSet();
                if (rs!=null){
                    while (rs.next()){
                        //System.out.println(rs.getString(1));
                        //bdListModel.addElement(rs.getString(1)); 
                        jComboBoxGrps.addItem(rs.getString(1));                    
                    }
                }
                s.close();
                rs.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
            jComboBoxGrps.addItem("Cadastrar Grupo...");
        }
    }
 
    public void CarregaUsers(int VarGrpIndex, int VarGrpItens){
        //System.out.println(VarGrpIndex);
        //System.out.println(VarGrpItens);
        destListModel.removeAllElements();    
        if (conexao.conectado){
            Statement s; 
            try {
                s = conexao.conn.createStatement();
                if (VarGrpIndex==0 || VarGrpIndex==VarGrpItens ){
                  s.execute("select * from destinatarios");
                } else {
                    s.execute("select * from destinatarios WHERE GrupoID="+VarGrpIndex);
                }
                ResultSet rs = s.getResultSet();
                if (rs!=null){
                    while (rs.next()){
                        destListModel.addElement(rs.getString(3));
                    }
                }
                s.close();
                rs.close();

            } catch (SQLException ex) {
                ex.printStackTrace();
            }  
        }
    }
    
